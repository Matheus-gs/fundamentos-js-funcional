// Anatomia da Função #01

function sayHello() {
    console.log('Hello')
}

function sayHelloTo(name) {
    console.log(`Hello ${name}`)
}

function returnHi() {
    return 'Hi'
}

function returnHiTo(name) {
    return `Hi ${name}`
}

sayHello()
sayHelloTo('Matheus')
console.log(returnHi())
console.log(returnHiTo('Matheus'))
